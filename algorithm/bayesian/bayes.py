def trainNB0(trainMatrix, trainCategory):
    numTrainDocs = len(trainMatrix)
    numWords = len(trainMatrix[0])
    pAbusive = sum(trainCategory) / float(numTrainDocs)  # 属于侮辱性文档的概率 P(1)
    p0Num = ones(numWords)  # 分子  拉普拉斯平滑
    p1Num = ones(numWords)  # change to ones()
    p0Denom = 2.0  # 分母
    p1Denom = 2.0  # change to 2.0
    for i in range(numTrainDocs):
        if trainCategory[i] == 1:
            p1Num += trainMatrix[i]
            p1Denom += sum(trainMatrix[i])
        else:
            p0Num += trainMatrix[i]
            p0Denom += sum(trainMatrix[i])
    p1Vect = log(p1Num / p1Denom)  # change to log()   P(wi|c1)=P(w0|c1) * P(w1|c1)  某个单词在某类文档中出现的概率向量
    p0Vect = log(p0Num / p0Denom)  # change to log()   避免向下溢出，转换成加法运算
    return p0Vect, p1Vect, pAbusive  # 两个向量和一个概率(是侮辱文档的概率)


